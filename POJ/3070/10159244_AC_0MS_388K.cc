#include <cstdio>

int n,r[2][2];

void f(int n)
{
	int s[2][2],i,j;
	if(n==1)return;
	f(n/2);
	for(i=0;i<2;i++)
	for(j=0;j<2;j++)
		s[i][j]=r[i][j];
	r[0][0]=(s[0][0]*s[0][0]+s[0][1]*s[1][0])%10000;
	r[0][1]=(s[0][0]*s[0][1]+s[0][1]*s[1][1])%10000;
	r[1][0]=(s[1][0]*s[0][0]+s[1][1]*s[1][0])%10000;
	r[1][1]=(s[1][0]*s[0][1]+s[1][1]*s[1][1])%10000;
	if(n%2)
	{
		for(i=0;i<2;i++)
		for(j=0;j<2;j++)
			s[i][j]=r[i][j];
		r[0][0]=(s[0][0]*0+s[0][1]*1)%10000;
		r[0][1]=(s[0][0]*1+s[0][1]*1)%10000;
		r[1][0]=(s[1][0]*0+s[1][1]*1)%10000;
		r[1][1]=(s[1][0]*1+s[1][1]*1)%10000;
	}
}

int main ()
{
	while(scanf("%d",&n) && n!=-1)
	{
		if(n==0)
		{
			printf("0\n");
			continue;
		}
		if(n==1)
		{
			printf("1\n");
			continue;
		}
		r[0][0]=0,r[0][1]=r[1][0]=r[1][1]=1;
		f(n-1);
		printf("%d\n",r[1][1]%10000);
	}
	
	return 0;
}
